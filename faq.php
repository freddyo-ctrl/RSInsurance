            </div>

            <div class="col-xs-12 faq-section">
                <h3>What does liability coverage mean?</h3>
                <p>Liability coverage means that in case of a vehicle accident in which you are at fault, the insurance company 
                   will pay for the damages of the people you had the accident with. This means that this policy does not cover 
                   your own personal or vehicle damages in case of an accident.</p>
            </div>

            <div class="col-xs-12 faq-section">
                <h3>What does full coverage mean?</h3>
                <p>Full coverage means that in case of an vehicle accident in which you are at fault, the insurance policy will 
                   pay for the damages of the people you had the accident with and will also pay for the damages your vehicle 
                   incurred during the accident. Full coverage also covers fires, floods, vandalism and hail/storm damage. 
                   However, full coverage requires a deductible and depending on how you purchased your vehicle, also a 
                   lienholder.</p>
            </div>

            <div class="col-xs-12 faq-section">
                <h3>What does deductible mean in terms of full coverage?</h3>
                <p>Deductible is a predetermined amount of money either chosen by you or by the agency you purchased the vehicle 
                   from at the time of insuring the vehicle. This predetermined amount of money needs to be paid out of pocket 
                   and given to the car insurance company so that they can take control in case your vehicle incurs damage either 
                   in an accident, fire, flood, hail/storm or vandalism. This happens so that the insurance company can take over 
                   and pay for the damages done to your vehicle.</p>
            </div>

            <div class="col-xs-12 faq-section">
                <h3>What does lienholder mean in terms of full coverage?</h3>
                <p>Lienholder means that the financing company with which you took a loan to purchase your vehicle wishes to appear 
                   in your policy. This is done so that the financing company is in the loop with any changes done to the policy 
                   which usually means that they are checking that the vehicle is still in your policy and that payments are being 
                   made on time.</p>
            </div>

            <div class="col-xs-12 faq-section">
                <h3>What does proof of insurance mean?</h3>
                <p>Proof of insurance means presenting your car insurance identification cards that show that you have been consistent 
                   in paying your car insurance on time.</p>
            </div>

